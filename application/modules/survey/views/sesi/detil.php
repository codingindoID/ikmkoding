            <h3>Mal Pelayanan Publik Dibuka dengan Pembatasan Kunjungan</h3>
            <p>
              Untuk memberikan pelayanan kepada masyarakat, Pemerintah Kabupaten (Pemkab) Jepara mulai membuka Mal Pelayanan Publik yang berada di Lantai I Gedung Organisasi Perangkat Daerah (OPD) bersama, Jalan Kartini Nomor 1 Jepara.

              Sekretaris Daerah (Sekda) Jepara Edy Sujatmiko, didampingi Asisten Administrasi Umum Sekda Jepara Sujarot, dan Kepala Dinas Penanaman Modal, Perijinan Terpadu Satu Pintu (DPMPTSP) Kabupaten Jepara Hery Yulianto, Kamis (2/7/2020) pagi melihat secara langsung kesiapan layanan yang sudah berjalan.

              “Mal Pelayanan Publik sebenarnya dibuka kemarin pada bulan April bertepatan dengan hari jadi kota Jepara. Namun karena pandemi Covid-19 akhirnya ditunda,” kata dia.

              Disampaikan Edy, kondisi Jepara saat ini masih tinggi kasus positif Covid-19. Namun di sisi lain masyarakat butuh pelayanan yang cepat dan maksimal. Sehingga Mal Pelayanan Publik ini dibuka secara bertahap. “Sudah kita buka sejak 1 Juli 2020, Namun dengan pembatasan kunjungan,” kata dia.

              Beberapa layanan yang sudah difungsikan yaitu gerai Kantor Samsat, Bank Jateng, dan PDAM Jepara. Sedangkan untuk gerai instansi yaitu Dinas Lingkungan Hidup (DLH), Dinas Pekerjaan Umum dan Perumahan Rakyat (DPUPR), Dinas Kesehatan Kabupaten (Dinkes), Dinas Kependudukan dan Catatan Sipil (Disdukcapil), DPMPTSP, Diskominfo, dan Diskop UKM Nakertrans Jepara. Sedangkan sejumlah gerai belum ada layanan.

              Edy mencontohkan untuk pelayanan Disdukcapil sudah bisa dilaksanakan. Meskipun ke depan, pelayanan ini akan dilakukan di semua kecamatan. “Kami ingin memberikan pelayanan terbaik di tengah pandemi Covid-19,” katanya.

              Kepala DPMPTSP Jepara Hery Yulianto mengatakan, Untuk memberikan kenyamanan dan keamanan bagi setiap pengunjung yang mengurus ijin, sudah disiapkan berbagai fasilitas sesuai dengan protokol kesehatan. Seperti tempat cuci tangan, dan pendeteksi suhu tubuh. Selain itu jarak antrean dan pelayanan juga diatur.

              “Tentunya ini berbeda dengan kondisi sebelumnya. Ada penyesuaian-penyesuaian dari pelayanan yang kita lakukan,” kata dia.

              Teller di Bank Jateng Cabang Jepara Henny mengatakan, sebelumnya sudah diinformasikan kepada para nasabah, sehingga mereka tidak merasa bingung. 
